# Breaking the long lines into readable form is done using triple single 
#or double quotes
##
def triple_quoter():
    para_str = """ This is a really long comment that can make  
the code look ugly and uncomfortable to read on 
a small screen so it needs to be broken into  
multi-line comments using double triple-quotes"""
    print(para_str)

if __name__ == "__main__":
    triple_quoter()
